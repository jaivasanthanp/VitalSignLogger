  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_ICACHE_Init();
  /* USER CODE BEGIN 2 */

  /* ── I2C bus scan — print which devices respond ──────── */
  printf("\r\nI2C1 bus scan:\r\n");
  for (uint8_t addr = 0x08; addr < 0x78; addr++) {
      if (HAL_I2C_IsDeviceReady(&hi2c1, addr << 1, 1, 10) == HAL_OK) {
          printf("  0x%02X found\r\n", addr);
      }
  }
  printf("Scan done.\r\n");

  /* ── Initialise I2C devices in bare-metal context before RTOS ── */
  SSD1306_Init(&hi2c1);
  SSD1306_Clear();
  SSD1306_DrawString(0, 0, "VITAL SIGN LOGGER");
  SSD1306_DrawString(0, 1, "-----------------");
  SSD1306_DrawString(0, 2, "Initialising...  ");
  SSD1306_Flush();

  MAX30102_Init(&ppg_dev, &hi2c1);

  /* MAX30102_Init does HAL_I2C_DeInit/Init after soft-reset —
   * re-init SSD1306 so its controller state is guaranteed clean */
  SSD1306_Init(&hi2c1);

  MPU6050_Init(&imu_dev, &hi2c1);
  if (imu_dev.init_ok) {
      SSD1306_Clear();
      SSD1306_DrawString(0, 0, "VITAL SIGN LOGGER");
      SSD1306_DrawString(0, 2, "IMU calibrating..");
      SSD1306_DrawString(0, 4, "Keep board flat!");
      SSD1306_Flush();
      MPU6050_Calibrate(&imu_dev, 200);

      /* I2C may be in error state after bulk reads — recover */
      HAL_I2C_DeInit(&hi2c1);
      HAL_I2C_Init(&hi2c1);
  }

  /* ── UART self-test: send a known string ─────────────── */
  const char *boot_msg = "\r\n=== VitalSignLogger boot ===\r\n";
  HAL_UART_Transmit(&huart1, (const uint8_t *)boot_msg, 31, 100);

  /* ── Show init results on OLED ──────────────────────── */
  SSD1306_Clear();
  SSD1306_DrawString(0, 0, "VITAL SIGN LOGGER");
  SSD1306_DrawString(0, 1, "-----------------");

  if (ppg_dev.init_ok)
      SSD1306_DrawString(0, 2, "PPG: OK");
  else
      SSD1306_DrawString(0, 2, "PPG: FAIL");

  if (imu_dev.init_ok)
      SSD1306_DrawString(0, 3, "IMU: OK");
  else
      SSD1306_DrawString(0, 3, "IMU: FAIL");

  SSD1306_DrawString(0, 5, "Place finger...  ");
  SSD1306_DrawString(0, 7, "UART: 115200 8N1");
  SSD1306_Flush();

  printf("PPG: %s  IMU: %s\r\n",
         ppg_dev.init_ok ? "OK" : "FAIL",
         imu_dev.init_ok ? "OK" : "FAIL");

  HAL_Delay(1500);   /* show result before tasks take over */

  /* USER CODE END 2 */

  /* Init scheduler */
  osKernelInitialize();
  /* Call init function for freertos objects (in app_freertos.c) */
  MX_FREERTOS_Init();

  /* Start scheduler */
  osKernelStart();

